    private void btnRegistrarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVehiculoActionPerformed

        String placa = txtPlaca.getText();
        String modelo = txtModelo.getText();
        String color = txtColor.getText();

        if (placa.isEmpty() || modelo.isEmpty() || color.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Llene los campos");

        } else {

            //recupera la lista de vehiculos
            List<Vehiculo> lista = usuario.getListaVehiculo();
            //a√±ade los objetos para la lista
            lista.add(new Vehiculo(placa, modelo, color, usuario));
            //le establece en el usuario
            usuario.setListaVehiculo(lista);

            try {
                //y lo manda a actualizar asi ya estaremos guardando en la bd.
                controladorUsuario.update(usuario);
                JOptionPane.showMessageDialog(this, "Se ha registrado con exito.");
            } catch (Exception ex) {
                Logger.getLogger(VentanaIngresarVehiculo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnRegistrarVehiculoActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String cedula = txtCedula.getText();

        usuario = controladorUsuario.buscar(cedula);

        if (usuario != null) {

            txtNombre.setText(usuario.getNombre());
            txtApellido.setText(usuario.getApellido());
            txtTipoUsuario.setText(usuario.getTipoUsuario());

        } else {

            JOptionPane.showMessageDialog(this, "No existe el usuario con el nuero de cedula: " + cedula);

        }


    }//GEN-LAST:event_btnBuscarActionPerformed


